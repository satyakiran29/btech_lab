DATA SEGMENT
     NUM1 DW 5678H
     NUM2 DW 1234H
     RESULT DW 08 DUP(0)
DATA ENDS
CODE SEGMENT
START:MOV AX,DATA
      MOV DS,AX
      CALL XYZ
      ADD  AX,BX
      MOV RESULT,AX
      CALL XYZ
      SUB AX,BX
      MOV RESULT+02H,AX
      HLT

      XYZ PROC
      MOV AX,NUM1
      MOV BX,NUM2
      RET
      XYZ ENDP
CODE ENDS
END  START

