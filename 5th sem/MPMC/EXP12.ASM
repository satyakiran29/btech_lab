ORG 0000H
BACK:LCALL DELAY
     MOV P2,#00H
     MOV P1,#00H
SCAN:JNB P0.0,COL1
     JNB P0.1,COL2
     JNB P0.2,COL3
     SJMP SCAN
COL1:MOV P1,#0FEH
     JNB P0.0,ONE
     MOV P1,#0FDH
     JNB P0.0,FOUR
     MOV P1,#0FCH
     JNB P0.0,SEVEN
COL2:MOV P1,#0FEH
     JNB P0.1,TWO
     MOV P1,#0FDH
     JNB P0.1,FIVE
     MOV P1,#0FCH
     JNB P0.1,EIGHT
COL3:MOV P1,#0FEH
     JNB P0.2,THREE
     MOV P1,#0FDH
     JNB P0.2,SIX
     MOV P1,#0FCH
     JNB P0.2,NINE
ONE:MOV P2,#06H
    SJMP DELAY
TWO:MOV P2,#5BH
    SJMP DELAY
THREE:MOV P2,#4FH
      SJMP DELAY
FOUR:MOV P2,#66H
     SJMP DELAY
FIVE:MOV P2,#6DH
     SJMP DELAY
SIX:MOV P2,#7DH
    SJMP DELAY
SEVEN:MOV P2,#07H
      SJMP DELAY
EIGHT:MOV P2,#7FH
      SJMP DELAY;
NINE:MOV P2,#67H
     SJMP DELAY
DELAY:	MOV TL0,#0F0H
	MOV TH0,#0FFH
	MOV TMOD,#01H
	SETB TR0
	HERE2:	JNB TF0,HERE2
	CLR TR0
	CLR TF0
	RET
